#include <string.h>

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char	*v;
	char	*w;

	if (n == 0 || s1 == s2)
		return (s1);
	v = (char *)s1;
	w = (char *)s2;
	while (--n)
		*v++ = *w++;
	*v = *w;
	return (s1);
}
